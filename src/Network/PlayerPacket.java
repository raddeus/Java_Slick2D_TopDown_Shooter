package Network;

public class PlayerPacket {
float x,y,rotation;
int ID;
public PlayerPacket(){
}

public float getX() {
	return x;
}

public void setX(float x) {
	this.x = x;
}

public float getY() {
	return y;
}

public void setY(float y) {
	this.y = y;
}

public float getRotation() {
	return rotation;
}

public void setRotation(float rotation) {
	this.rotation = rotation;
}

public int getID() {
	return ID;
}

public void setID(int iD) {
	ID = iD;
}



}
